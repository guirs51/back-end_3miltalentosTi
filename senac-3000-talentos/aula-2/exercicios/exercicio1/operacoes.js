// Função para somar dois números
function soma(a, b) {
  return a + b;
}

// Função para subtrair dois números
function subtrai(a, b) {
  return a - b;
}

// Função para multiplicar dois números
function multiplica(a, b) {
  return a * b;
}

// Exportando as funções
module.exports = {
  soma,
  subtrai,
  multiplica,
};
